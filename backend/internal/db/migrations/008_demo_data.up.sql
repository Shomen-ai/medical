-- ── Doctor schedules: May + June 2026, Mon-Sat 09:00-18:00, Sundays + 18 May off
INSERT INTO doctor_schedules (doctor_id, work_date, start_time, end_time, is_day_off)
SELECT d.id,
       dt::date,
       '09:00'::time,
       '18:00'::time,
       (EXTRACT(DOW FROM dt) = 0) OR (dt::date = DATE '2026-05-18')
FROM doctors d
CROSS JOIN generate_series(DATE '2026-05-01', DATE '2026-06-30', INTERVAL '1 day') AS dt
ON CONFLICT (doctor_id, work_date) DO UPDATE SET
  start_time = EXCLUDED.start_time,
  end_time   = EXCLUDED.end_time,
  is_day_off = EXCLUDED.is_day_off;

